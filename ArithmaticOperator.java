package codeforwin;

public class ArithmaticOperator {
	
	public static void main(String[] args) {
		
		int a=10;
		
		int b=5;
		
		System.out.println("Sum =" +(a+b));      // sum
		
		System.out.println("Difference =" +(a-b));  // difference(minus) // formula =large number - small number (a-b)
		
		System.out.println("Product =" +(a*b)); // product(multiply) // formula = number1 * number2 (a*b)        
		
		System.out.println("Quotient  =" +(a/b)); // divide pannah mela varum that is (quotient) 
		
		System.out.println("Modulous  =" +(a%b)); // divide pannah kizha  varum that is (modulous) 
		
	}

}
