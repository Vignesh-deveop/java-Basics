package codeforwin;

public class Inputoutput {

	public static void main (String[] args) {
		
		char a='c';                          //2 bytes / single character
		
		System.out.println("char value is "+a); 
		
		byte b=127;                         //1 / -128 to 127
		
		System.out.println("byte value is" +b); 
		
		short c=32767;                    //2/  -32768 to 32767
		
		System.out.println("short value is "+ c);
		
		int d=5353637;                         //4 //-2^31 to 2^31-1
		
		System.out.println("int value is "+d);
		
		
		long e=64474737848l;
		
		System.out.println("long value is "+e);               //8 // -2^63 to 2^63-1
		
		float f= 22.46f; 
		
		System.out.println("float value is "+f);             // 4  // upto 7 decimal digits
		
		
		 double g=354646.74646;                                    // 8  // upto 15 decimal digits
		
		System.out.println("double value is "+g);
		
		
		boolean h= true;
		
		System.out.println("boolean value is "+h);           //1 // true or false
		
		
	//	long long h=45465468468464654654654l;                                     ask long long and long double
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
}
